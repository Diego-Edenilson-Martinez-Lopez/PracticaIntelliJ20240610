package Matematica;

public class Calculadora {

    public int sumar (int a, int b) {
        int Resultado = a + b;
        return Resultado;
    }

    public int restar (int a, int b) {
        int Resultado = a - b;
        return Resultado;

    }
    public int multiplicar (int a, int b) {
        int Resultado = a * b;
        return Resultado;
    }
    public int dividir (int a, int b) {
        int Resultado = a / b;
        return Resultado;
    }

}
